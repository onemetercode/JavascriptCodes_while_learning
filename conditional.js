const age = 70;
//EQUALS
if (age == 70){
    console.log('true');
}else{
    console.log('false');
}
//NOT EQUALS
if (age != 60){
    console.log('true');
}else{
    console.log('false');
}
//IF ELSE IF
if (age > 70){
    console.log('true');
}else if(age < 70){
    console.log('false');
}else if(age == 70){
    console.log('true');
}else{
    console.log('end this');
}
//identical and not identical
if(age === '70'){
    console.log('true');
}else if(age !== '70'){
    console.log('this is not identical');
}else{
    console.log('identical');
}


